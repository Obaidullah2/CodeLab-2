#Using math module compute the following

#For a =2.3 find the ceil of a
#For a =2.3 find the floor of a
#For a = 5 find the factorial of a
#Find the value of 23
#For a=16 find the square root of a

import math

# Find the ceil of a
a_ceil = 2.3
ceil_result = math.ceil(a_ceil)
print(f"Ceil of {a_ceil} is: {ceil_result}")

# Find the floor of a
a_floor = 2.3
floor_result = math.floor(a_floor)
print(f"Floor of {a_floor} is: {floor_result}")

# Find the factorial of a
a_factorial = 5
factorial_result = math.factorial(a_factorial)
print(f"Factorial of {a_factorial} is: {factorial_result}")

# Find the value of 2^3
power_result = math.pow(2, 3)
print(f"2^3 is: {power_result}")

# Find the square root of a
a_sqrt = 16
sqrt_result = math.sqrt(a_sqrt)
print(f"Square root of {a_sqrt} is: {sqrt_result}")
